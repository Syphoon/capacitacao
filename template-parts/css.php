	<blockquote class="title">Como funciona: CSS</blockquote>
	<div id="css_1" style=" background-color: rgba(255,255,255,0.5);" class=" exp">
		
		<p>O css é utilizado para estilizar o ambiente desejado, se compatível. Nele pode ser definido propriedades que vão ser utilizadas no ambiente, e que no mesmo podem ser chamadas utilizando a sintaxe correta, e assim torna-se possível o desenvolvimento de uma aplicação agradável aos olhos.</p>
		<p>Normalmente os elementos mais utilizados do css são as de classes e id. Nestes podemos definir propriedades de margem ( interna , externa ), cor de fonte e outros. Todas as propriedades do css são definidas em inglês, por isso um conhecimento básico de inglês te proporcionaria uma grande facilidade no aprendizado. (Margem => margin, Cor => color, etc). </p>

	</div>

	<blockquote class="title">Definições: CSS</blockquote>
	<div  style=" background-color: rgba(255,255,255,0.5);"  class="exp">
		<p>Classes e ID's são identificadores do css que podem ser utilizados no html para melhor estilização do projeto. As classes são identificadores 'globais', ou seja, podem sera tribuidos a mais de um elemento, diferente dos ID'S que são identificações unicas de cada elemento. Os ID's por serem identificadores únicos tem suas 'vantagens', a sua prioridade é maior em relação aos outros identificadores, ou seja, eles irão sobrepor qualquer propriedade 'repetida' em algum outro identificador.</p>
	</div>

